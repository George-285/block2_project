DROP TABLE IF EXISTS names_updated;
-- Создание новой таблицы с дедуплицированными именами и новыми new_id через ROW_NUMBER()
CREATE TABLE names_updated AS
SELECT ROW_NUMBER() OVER (ORDER BY MIN(id)) AS new_id, pname
FROM names
GROUP BY pname;

SELECT * FROM names_updated;
