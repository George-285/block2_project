-- Нормализование имён через TRANSLATE + INITCAP с фильтрацией через WHERE   
UPDATE names
SET pname = INITCAP(TRANSLATE(pname,
    'aocepxykκABCEHKMOPTX',
    'аосерхуккАВСЕНКМОРТХ'
))
WHERE pname !~ '^[А-яЁё]+$'
   OR pname <> INITCAP(LOWER(pname));

